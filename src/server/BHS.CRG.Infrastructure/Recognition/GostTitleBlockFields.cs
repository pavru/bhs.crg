/// реестра листов; графы 10-13 (должности/фамилии/подписи разработчиков) не включены —
/// не нужны для реестра. Path — JSON-ключ, который должен вернуть распознаватель (без
/// пробелов, как и в остальных RecognitionField по проекту — см. QualityDocs).
/// </summary>
public static class GostTitleBlockFields
{
    public static readonly IReadOnlyList<RecognitionField> All =
    [
        new("Шифр", "Обозначение документа (шифр)", "string"),
        new("ОбъектСтроительства", "Наименование объекта строительства", "string"),
        new("ЗданиеСооружение", "Наименование здания (сооружения) или вид работ", "string"),
        new("НаименованиеИзображения", "Наименование изображений на листе", "string"),
        new("НаименованиеДокумента", "Наименование документа", "string"),
